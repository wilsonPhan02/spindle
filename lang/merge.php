function updateFile($path, $translations) {
    $content = json_decode(file_get_contents($path), true);
    foreach ($translations as $k => $v) {
        if (!isset($content[$k])) {
            $content[$k] = $v;
        }
    }
    file_put_contents($path, json_encode($content, JSON_PRETTY_PRINT | JSON_UNESCAPED_UNICODE));
}

